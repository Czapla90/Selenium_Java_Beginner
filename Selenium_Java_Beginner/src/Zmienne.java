// Zmienna to zarezerwowane miejsce w pamięci, z określonym typem danych.
public class Zmienne {


    public static void main(String[] args) {
        String name = "Mateusz";
        int firstNumber = 1;
        int secondNumber = 2;
        int result = firstNumber + secondNumber;

        int forthNumber, fifthNumber;
        forthNumber = 5;
        fifthNumber = 8;

        name = "Matthew";
        System.out.println(name);
        System.out.println(result);
        System.out.println(forthNumber - fifthNumber);
    }
}
